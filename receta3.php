            <h2>Ingredients</h2>
            <ul>
                <li>¼ cup Butter melted and cooled</li>
                <li>1 cup Granulated sugar</li>
                <li>2 Eggs</li>
                <li>½ teaspoon Kosher salt</li>
                <li>2 teaspoons Vanilla extract</li>
                <li>¼ cup Vegetable oil</li>
                <li>½ cup Milk</li>
                <li>¼ cup Greek yogurt</li>
                <li>2 ⅓ cups Flour</li>
                <li>2 teaspoons Baking powder</li>
                <li>½ teaspoon Baking soda</li>
                <li>½ cup Chocolate chips</li>
                <li>½ cup Mini chocolate chips</li>
            </ul>

            <h2>Instructions</h2>
            <ol>
                <li>Preheat an oven to 180°C (350°F) and prep a 12-cup muffin pan with paper liners.</li>
                <li>Into a large mixing bowl, add the cooled and melted butter, eggs, Greek yogurt, kosher salt, vanilla extract, milk, granulated sugar, and vegetable oil, and mix until combined.</li>
                <li>Add the flour, baking powder, baking soda, and chocolate chips. Mix until just combined and there are no visible lumps of flour. Do not overmix the batter to keep them moist.</li>
                <li>Let the batter sit at room temperature for at least 30 minutes. This step creates the signature domed tops that the best muffins have!</li>
                <li>Scoop the batter with a large ice cream scooper into the muffin tins, filling them almost completely.</li>
                <li>Top the muffins with more chocolate chips (optional). Bake in a 200°C (400°F) preheated oven for 5 minutes, and then bake for another 10-12 minutes at 180°C (350°F) or until a toothpick comes out clean.</li>
            </ol>

            <h2>Notes</h2>
            <ul>
                <li>Be sure to use room temperature ingredients, such as eggs. This step is crucial for achieving a homogeneous mixture that bakes perfectly.</li>
                <li>Don’t overmix the batter, you don’t want to develop the gluten in the flour. This step ensures you have moist muffins and not dense and dry muffins.</li>
                <li>For that bakery-style look, sprinkle some extra chocolate chips on top of each muffin before baking. It adds a nice touch and extra burst of chocolate flavor.</li>
                <li>Use measuring cups and spoons, or even better, a kitchen scale for accuracy when measuring flour, sugar, and other ingredients. Baking is a science, so precise measurements matter.</li>
                <li>Measure your flour correctly! When measuring your flour, avoid scooping it with a measuring cup. Instead, stir the flour with a spoon to create as many air pockets as possible, and spoon it into your measuring cup. Finally, level it with a back of a knife.</li>
                <li>Make sure that you are using fresh ingredients! One of the most common mistakes in baking and making muffins is not noticing that your baking powder or baking soda is expired. This can ruin your muffins and waste your time and money on the ingredients.</li>
                <li>Be patient! I know it’s extremely tempting to eat these muffins as they are pulled out of the oven, but let these muffins cool for at least 15 minutes so they will be firmer and won’t crumble up.</li>
            </ul>
