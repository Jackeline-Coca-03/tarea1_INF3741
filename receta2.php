            <h2>Ingredients</h2>
            <ul>
                <li>¼ cup Butter melted and cooled</li>
                <li>1 teaspoon Vanilla extract</li>
                <li>1 Egg</li>
                <li>⅓ cup Granulated sugar</li>
                <li>½ cup Brown sugar</li>
                <li>½ teaspoon Kosher salt</li>
                <li>½ cup Greek yogurt</li>
                <li>½ cup Cocoa powder</li>
                <li>1 ½ cups Canned pumpkin puree</li>
                <li>1 cup Flour</li>
                <li>1 teaspoon Baking soda</li>
                <li>1 cup Chocolate chips</li>
            </ul>

            <h2>Instructions</h2>
            <ol>
                <li>Preheat an oven to 180°C (350°F) and prep a loaf pan with parchment paper.</li>
                <li>Into a large mixing bowl, add the pumpkin puree, Greek yogurt, vanilla extract, cooled melted butter, kosher salt, egg, granulated sugar, brown sugar, and sift the cocoa powder. Mix with a whisk until well combined.</li>
                <li>Add the flour, and baking soda, and mix until almost combined.</li>
                <li>Add the chocolate chips and mix until just combined and there are no lumps of flour visible.</li>
                <li>Pour the batter into a parchment paper-lined loaf pan and top the batter with extra chocolate chips!</li>
                <li>Bake in a 180°C (350°F) preheated oven for 50-60 minutes or until a toothpick inserted into the bread comes out clean.</li>
            </ol>

            <h2>Notes</h2>
            <ul>
                <li>Use room temperature ingredients. It is important that you use room-temperature ingredients like eggs and butter. This step ensures that your mixture mixes into a homogenous mixture and bakes perfectly. You don’t want to place cold batter into the oven.</li>
                <li>Measure your flour correctly! When measuring your flour, avoid scooping it with a measuring cup. Instead, stir the flour with a spoon to create as many air pockets as possible, and spoon it into your measuring cup. Finally, level it with the back of a knife.</li>
                <li>Don’t overmix the batter, you don’t want to develop the gluten in the flour. This step ensures you have a moist cake and not a dense and dry cake.</li>
                <li>Line your baking pan with parchment paper! This step helps the cake to bake evenly, and it makes sure that your loaf cake will release easily from the pan.</li>
                <li>Be patient! I know it’s extremely tempting to eat this cake as it is pulled out of the oven, but let the loaf cool for at least 20 minutes so it will be more firm and won’t crumble up. This cake is extra moist and melts in your mouth delicious, so it will be worth it to wait these 20 minutes.</li>
            </ul>
