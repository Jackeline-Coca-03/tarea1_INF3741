            <h2>Ingredients</h2>
            <h4>For the dough</h4>
            <ul>
                <li>5 cups Flour AP or bread flour</li>
                <li>1 tablespoon Instant yeast</li>
                <li>1 teaspoon Kosher salt</li>
                <li>⅓ cup Granulated sugar</li>
                <li>1 ¼ cups Milk</li>
                <li>¼ cup Butter melted and cooled, unsalted</li>
                <li>2 Eggs</li>
            </ul>

            <h4>For the chocolate filling</h4>
            <ul>
                <li>3 oz Butter unsalted, room temp</li>
                <li>3 tablespoon Cocoa powder</li>
                <li>½ cup Powder sugar</li>
                <li>1 teaspoon Vanilla extract</li>
                <li>5.3 oz Dark chocolate</li>
                <li>1 teaspoon Cinnamon optional</li>
            </ul>

            <h4>For the chocolate cream cheese glaze</h4>
            <ul>
                <li>¼ cup Butter unsalted, room temp</li>
                <li>4.2 oz Cream cheese room temp</li>
                <li>¼ teaspoon Kosher salt</li>
                <li>1 teaspoon Vanilla extract</li>
                <li>1 ½ cups Powdered sugar</li>
                <li>⅓ cup Cocoa powder</li>
            </ul>

            <h2>Instructions</h2>
            <ol>
                <li>In a bowl of a stand mixer, add the flour, dry yeast, and granulated sugar. Mix with a spoon until combined. Add the salt, and mix until combined.</li>
                <li>Add the eggs, room-temperature milk, and cooled melted butter. Start mixing on low until the dough forms.</li>
                <li>Increase the speed to medium and mix for at least 8 minutes or until the dough is smooth and silky.</li>
                <li>Cover the bowl with plastic wrap and let the dough rise at room temperature for 1-2 hours or until it’s doubled in size (or even tripled in size).</li>
            </ol>

            <h2>Notes</h2>
            <ul>
                <li>Use room temperature ingredients. It is important that you use room-temperature ingredients like eggs and milk. This step ensures that your dough mixes into a homogenous mixture and bakes perfectly.</li>
                <li>Measure your flour correctly! When measuring your flour, avoid scooping it with a measuring cup. Instead, stir the flour with a spoon to create as many air pockets as possible, and spoon it into your measuring cup. Finally, level it with the back of a knife.</li>
                <li>Don't overheat your milk! Overheating your milk is one of the most common mistakes in making cinnamon rolls. If you heat your milk over 110f, it might kill your yeast and stop your dough from rising.</li>
                <li>Make sure that your ingredients are fresh! Make sure that your flour and yeast are fresh and not expired. This step ensures that you will have great dough to work with.</li>
                <li>Be patient! Let the dough rise at least until it doubles its size. The more the dough rises, the more fluffy the cinnamon rolls will be.</li>
                <li>Make sure that your yeast isn't expired. You can check if your yeast is expired by mixing a teaspoon of dry yeast with ¼ cup of room temperature water, waiting 10 minutes, and if the yeast has bubbles in it and it puffed a bit, your yeast is alive and good, if there are no bubbles in your yeast mixture, your yeast is probably expired.</li>
            </ul>
