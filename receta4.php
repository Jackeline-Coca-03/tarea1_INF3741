            <h2>Ingredients</h2>
            <ul>
                <li>1 loaf White bread</li>
                <li>5 Eggs</li>
                <li>50 g Butter melted and cooled</li>
                <li>2 teaspoons Vanilla extract</li>
                <li>½ teaspoon Kosher salt</li>
                <li>½ cup Granulated sugar</li>
                <li>¼ cup Brown sugar</li>
                <li>2 ½ cups Milk</li>
                <li>½ cup Heavy cream</li>
                <li>1 teaspoon Cinnamon</li>
                <li>⅓ teaspoon Nutmeg</li>
                <li>1 ½ cups Chocolate chips</li>
            </ul>

            <h2>Instructions</h2>
            <ol>
                <li>Preheat an oven to 180°C (350°F) and prepare a 9 by 13 pan with softened butter.</li>
                <li>Into a large mixing bowl, add the eggs, milk, heavy cream, sugar, brown sugar, cinnamon, nutmeg, melted and cooled butter, kosher salt, and vanilla extract, and mix with a whisk until well combined.</li>
                <li>Cut your bread into cubes and add it to the custard mixture. Gently mix until all the bread cubes are coated with the custard!</li>
                <li>Pour ½ of the bread mixture into the 9 by 13 baking dish and top it with ½ of the chocolate chips. Spread the chocolate chips using a spatula. This step prevents the chocolate chips from sinking into the bottom of the pan.</li>
                <li>Top with the rest of the bread mixture and top it with the rest of the chocolate chips. Allow the mixture to soak for 15 minutes before baking.</li>
                <li>Bake in a 180°C (350°F) preheated oven for 50-60 minutes or until the top of the casserole is golden brown, the chocolate chips are melted and the custard is fully baked.</li>
                <li>Remove from the oven and let it cool for 10-15 minutes, slice and serve with maple syrup, fresh fruit, or vanilla sauce!</li>
            </ol>

            <h2>Notes</h2>
            <ul>
                <li>Use room temperature ingredients. It is important that you use room-temperature ingredients like eggs and milk. This step ensures that your mixture mixes into a homogenous mixture and bakes perfectly.</li>
                <li>Ensure that you coat every piece of bread with the custard to prevent dry parts in your casserole.</li>
                <li>If you are using old stale bread, it might need more time for the custard to soak into the bread, so let it sit for longer. If you are using fresh bread, you can bake it immediately.</li>
                <li>If your bread pudding browns too quickly, cover it with foil to prevent it from browning quickly, then remove it 10 minutes before it's ready to let it get to a golden brown color.</li>
            </ul>
